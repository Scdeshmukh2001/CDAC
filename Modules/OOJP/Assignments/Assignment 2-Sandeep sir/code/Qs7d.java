public class Qs7d {
    public static void main(String[] args) {
        // Declare a method-local variable of type double
        double number = 123.456;

        // Convert the double to a String using Double.toString(double)
        String numberAsString = Double.toString(number);

        // Print the String representation of the double
        System.out.println("The double value as a String is: " + numberAsString);
    }
}

